# Arithmetic Operators
print(5 + 2)  # Addition
print(5 - 2)  # Subtraction
print(5 * 2)  # Multiplication
print(5 / 2)  # Division
print(5 % 2)  # Modulus
print(5//2)  # Floor division
print(5 ** 2)  # Exponentiation

# Assignment Operators
x = 5
x += 3  # x = x + 3
x -= 3  # x = x - 3
x *= 3  # x = x * 3
x /= 3  # x = x / 3
x **= 3  # x = x ** 3

# Comparison Operators
print(5 == 2)  # Equal
print(5 != 2)  # Not equal
print(5 > 2)  # Greater than
print(5 < 2)  # Less than
print(5 >= 2)  # Greater than or equal to
print(5 <= 2)  # Less than or equal to

# Logical Operators
print(True and False)  # AND
print(True or False)  # OR
print(not True)  # NOT

# Identity Operators
x = 5
y = 5
print(x is y)  # Identity
print(x is not y)  # Non-identity

# Membership Operators
my_list = [1, 2, 3]
print(2 in my_list)  # In
print(4 not in my_list)  # Not in


# Bitwise Operators
print(5 & 3)  # AND
print(5 | 3)  # OR
print(5 ^ 3)  # XOR
print(~5)  # NOT
print(5 << 2)  # Left shift
print(5 >> 2)  # Right shift
